// ── Browser Auto-Navigation Script ────────────────────────────
// Paste this into the browser console for auto-advancing demo
const BROWSER_SCRIPT = `
// SalesIQ Auto-Demo — paste in browser console on /dashboard
(async function salesiqAutoDemo() {
  const steps = ${JSON.stringify(
    DEMO_STEPS.map((s) => ({ route: s.route, title: s.title, duration: s.duration })),
    null,
    2
  )};

  function parseDuration(d) {
    const match = d.match(/(\\d+)/);
    return match ? parseInt(match[1]) * 1000 : 30000;
  }

  function showOverlay(step, index, total) {
    let overlay = document.getElementById('demo-overlay');
    if (!overlay) {
      overlay = document.createElement('div');
      overlay.id = 'demo-overlay';
      overlay.style.cssText = 'position:fixed;bottom:24px;right:24px;z-index:9999;background:#1e293b;color:white;padding:16px 24px;border-radius:16px;font-family:system-ui;box-shadow:0 20px 40px rgba(0,0,0,0.3);max-width:320px;';
      document.body.appendChild(overlay);
    }
    overlay.innerHTML = \`
      <div style="font-size:10px;color:#94a3b8;margin-bottom:4px;">DEMO \${index + 1}/\${total}</div>
      <div style="font-size:14px;font-weight:600;margin-bottom:8px;">\${step.title}</div>
      <div style="height:3px;background:#334155;border-radius:2px;overflow:hidden;">
        <div style="height:100%;background:linear-gradient(90deg,#3b82f6,#6366f1);width:\${((index + 1) / total) * 100}%;transition:width 0.5s;"></div>
      </div>
    \`;
  }

  for (let i = 0; i < steps.length; i++) {
    const step = steps[i];
    showOverlay(step, i, steps.length);
    window.history.pushState({}, '', step.route);
    window.dispatchEvent(new PopStateEvent('popstate'));
    await new Promise(r => setTimeout(r, parseDuration(step.duration)));
  }

  const overlay = document.getElementById('demo-overlay');
  if (overlay) overlay.remove();
  console.log('Demo complete!');
})();
`;

// Run if executed directly
if (typeof require !== "undefined" && require.main === module) {
  runConsoleDemo();
}

